package com.methods;

public class ClassOverload {
	
	
	
	
	public int calcoloAreaRettangolo (int lato1,int lato2) {
		
		
		return lato1*lato2;
		
	}
	
	
	
	public double calcoloAreaRettangolo (double lat1,double lat2 ) {
		
		
		return lat1+lat2;
	}
	
	
	public float calcoloAreaRettangolo(float lt1,float lt2) {
		
		
		return lt1*lt2;
		
	}

}
