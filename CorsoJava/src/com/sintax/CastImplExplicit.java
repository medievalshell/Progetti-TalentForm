package com.sintax;

public class CastImplExplicit {
	
	public static void main(String[] args) {


        //CAST IMPLICITO


        int a = 5;
        float b = a;
        System.out.println(b);


        //CAST EXPLICIT

        double d1 = 100;
        float f2 = (float) d1;

        System.out.println(f2);


    }

}
